def get_color(hue: Range12, lightness: Range10, saturation: Range10) -> Color:
    """from https://noacubestudio.github.io/blog.html"""
    idx = hue * 100 + saturation * 10 + lightness
    hex_code = COLORS[idx]
    return Color.from_uint24(hex_code)
